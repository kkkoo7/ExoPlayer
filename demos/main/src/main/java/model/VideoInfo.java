package model;

public class VideoInfo {
    private int mId;
    private String mTitle;
    private String mUrl;

    public int getmId() {
        return mId;
    }

    public String getmTitle() {
        return mTitle;
    }

    public String getmUrl() {
        return mUrl;
    }

    public void setmId(int mId) {
        this.mId = mId;
    }

    public void setmTitle(String mTitle) {
        this.mTitle = mTitle;
    }

    public void setmUrl(String mUrl) {
        this.mUrl = mUrl;
    }
}
